public class StringObjects {
    public static void main(String[] args) {

        String phrase = "Struggle City";
        System.out.println(phrase.substring(0,phrase.indexOf(" ")));
        System.out.println("This is my uppercase test " + phrase.toUpperCase());
        System.out.println("This is my lowercase test" + phrase.toLowerCase());
        System.out.println("This is my char test" + phrase.charAt(5));



    }
}